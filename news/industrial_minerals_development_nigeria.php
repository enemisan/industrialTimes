                        <h1>Nigeria To Spend USD123.5BN On Industrial Minerals Development</h1>
                        <div class="author-date">
                            <p>Industrial Times</p>
                            <div class="line"></div>
                            <p class="date">12 - 06 - 2023</p>
                        </div>
                    </div>
