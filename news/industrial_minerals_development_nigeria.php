                <section class="post-ctn">
                    <p class="post">The Ministry of Mines and Steel Development has estimated that over USD 123.5 billion will be required for the development of the country’s industrial minerals over the next five years (2023-2028).<br />
<br />
Project Coordinator, MinDiver, Dr. Sallim Salaam, who discloses this, said that Nigeria is richly endowed with over 30 industrial mineral types found in about 752 locations.<br />
<br />
He said that these minerals are at different stages of development, from exploration to mining, adding that the<br />
key participants in the industrial minerals sub-sector are artisanal and small-scale operators who exploit feldspar, trona, kaolin, talc, silica sand and dolomite for the chemical polymer and pharmaceutical industries.<br />
&quot; Others are quarry operators that mine and process granites, limestone, and marble as aggregates for the construction industry, cement, and lime production.<br />
<br />
&quot; Also included are barite and mica for mud drilling in the oil industry and phosphates for fertilizer production and soil liming.<br />
<br />
“Generally, the local production of industrial minerals has steadily grown from 43,725,070 tonnes in 2016 to 78 454,628 tonnes in 2021.<br />
<br />
&quot; Even at this level of progress, Nigeria currently imports over 80 percent of its industrial minerals for the local industries.<br />
<br />
&quot; The Federal Government is determined to develop the available industrial minerals to stimulate industrial growth further and for import substitution, ” he said.<br />
<br />
The estimated cost for implementing the actions described in the roadmap is 123.5 USD, to be spent over five years, the direct economic benefit obtained from the substitution of mineral imports covers this cost.<br />
<br />
Salaam said: “Nigeria’s drive to diversify its economy by leveraging on the high potential of natural resources is a priority programme of the Federal Government, and which is in line with the African Union’s sustainable development vision of  the proper utilization of the natural resource endowment of the nations on the continent, particularly industrial minerals commonly used as raw materials in industries.<br />
<br />
“One of the primary goals of the new roadmap is to create and implement a strategy to promote the domestic supply of mineral raw materials for local industries. The most relevant industrial minerals identified in the roadmap, as being exploited in Nigeria are minerals used in the construction industry: limestone, laterite, sand, clay, shale, dolomite, marble and granite.<br />
<br />
A second relevant group includes minerals used by industrial chains such as calcium carbonate and lime for water treatment, manganese and dolomite for steel making and iron cast. Others are kaolin and feldspar for the ceramics industry and mica for the electrical and painting industries.”<br />
<br />
Lamenting over Nigeria’s high dependence on imports of industrial minerals, Salaam added that Nigeria in 2016 imported more than 51,000t of calcium carbonate and lime, alongside substantial imports of these minerals, exceeded 28 million USD,60 per cent of industrial minerals.<br />
Alongside the construction and water treatment sectors, the industrial minerals imported are used in steel, oil and gas and a wide range of other industries.<br />
<br />
Nigeria has a mineral endowment appropriated to meet the domestic demand for industrial minerals in volume and quality, and the roadmap aims to reduce Nigeria’s import dependence on industrial minerals.”<br />
<br />
The Minister of Mines and Steel Development, Arc Olamilekan Adegbite, while speaking recently on Local Barite Development in Nigeria, explained that the Industrial Minerals Roadmap will optimize Nigeria’s industrial minerals to meet the standards of the manufacturing, industrial, and construction industry, while significantly reducing import dependency.</p>
