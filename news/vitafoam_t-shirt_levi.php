                    <p class="post">Mr. Taiwo Adeniyi is  the Group Managing Director, Vitafoam  Nigeria Plc.<br />
In this interview with journalists recently, he spoke  on the challenges and the rewards of doing business in Nigeria. <br />
He also bares his mind on how the  governments can encourage indusustrialists in the country.<br />
<br />
Are there any areas you’d like the incoming administration to address to enhance manufacturing?<br />
<br />
The incoming administration should address foreign exchange, insecurity and energy challenges currently facing the nation’s economy.<br />
<br />
We are expecting the government to bring in an economist to look at the impact of the various decisions as regards foreign exchange on the economy, rather than an individual putting whatsoever it is, imagining it will operate for everybody. What we are asking the government to attend to is the dichotomy between the various foreign exchange windows that we have in Nigeria.<br />
The second, is the insecurity in the country.<br />
Everyone loves to do business in an environment that is safe.<br />
<br />
Many of us produce from factories all over the nation. Moving our products from one place to another is becoming extremely difficult. Drivers now tell you that they cannot go to certain places and those that are ready to go are charging above the roof.<br />
<br />
This is making all of the cost elements go up from time to time. Third on the list is energy.<br />
If you do a combination of these three elements, you will find out that if you cast your mind back to what has helped the nation to continue, these three elements form the major part of what has sustained the economy.<br />
<br />
What has been the benefit of the privatisation of the energy sector?<br />
<br />
I don’t know whether ordinary people will say they are benefiting from it. But in terms of industry, we have not benefited so much from it. We remove subsidies and we have been coping with our lives. But because of the pressure on foreign exchange, diesel has now gone up the roof. Infrastructure is the language of the day. If you involve the private sector and the government to signal trust in the administration of the Public- Private Partnerships (PPP), I’m sure there’ll be a lot more participation. Because at the end of the day, it is good for all but what you find is that those at the private sector do not trust the government. You find these deficits in infrastructure all over the place, and it is making it so impossible to run a business. Cost is just skyrocketing by the day because it is what you buy that you sell.<br />
<br />
What other areas do you want the government to tackle in the manufacturing sector?<br />
<br />
You know, BoI gives loan to manufacturers and they do it excellently well. When BOI started giving out loans, I remember very well, for instance, Vitafoam Nigeria got a loan from BoI.<br />
One of the things that BoI used to do then was they would make a list of your foreign exchange requirements to CBN.<br />
<br />
The CBN will grant a window to genuine manufacturers because they know that when you do that, you are able to source your materials cheaply and bring them to this environment. By doing so, you have created job opportunities and expanded the horizon.<br />
I may not be absolutely correct. But I can also be sure that the foreign exchange allow-ances to the lawmakers and judges have never failed one day getting to them; I may not be too sure.<br />
<br />
The lawmakers and judges are not shouting that the CBN has not paid them their foreign exchange allowances as we in the manufacturing sector are crying to the government. So why wouldn’t the government create a special window where genuine manufacturers can assess foreign exchange? The government knows them, appraises them and benchmarks them. The government has implemented all manner of policies and yet it is not working. You find out that those items that are even on the exclusive list, get to this environment, cheaper than those of us that are produced locally because the windows manufacturer gets foreign exchange and makes a mess of it. I always say that in the wisdom of the fathers of this country and even the wisdom of this present government, they should invest heavily in agric. But between us who measures that window and who is it that monitors that If I as an agric investor take out 20 tons of garlic and I sell it and I get $20, do I bring $20 back to this environment? And even when I bring the $20 back to this environment, how do I sell the $20? That $20 is what I am supposed to make available for CBN to redistribute back into the economy. This is not happening as we speak. It is expected a special window is created for the manufacturers for them to be able to have access to foreign exchange to operate their business.<br />
<br />
How is Vitafoam able to consistently remain profitable in spite of all these headwinds?<br />
<br />
We always pride ourselves .<br />
You may see two p r o d u c t s in the m a r k e t a n d they look so much alike.<br />
<br />
But you ask yourself, why is it that they don’t cost the same price? There are intrinsic values we have in our products that stand out in the marketplace. We say to ourselves that our definition of quality is fit for purpose.<br />
<br />
For as long as our product fits the purpose for which you have gone to the market to spend your money, you will continue to buy.<br />
So, what we have done is to create a niche market for ourselves. We don’t play in every market. We don’t run the same race as others.<br />
<br />
We have created a niche market for ourselves to say when you buy our product, you will not regret spending your money and that is what is standing us out in the marketplace. And that’s why we produce well and we price well in the market.<br />
<br />
How will you describe the group’s 2022 Financial Year performance despite the challenges?<br />
<br />
The group performance declined by 2.2 percent in terms of profit, but the top line grew by 31 percent.<br />
We started the year with N192/litre of diesel. If you’ve got to look at the administrative costs or you look at the elements that contributed to that drop, you’ll find it under the likes of electricity and finance cost.<br />
<br />
Now I say electricity, tariffs went up in the course of the year, and diesel prices went up in the course of the year. Now, also, you’ll find out maintenance of machines. The way it works, those machines are not made here; so the same dollar you are struggling to get to buy your raw material is the same dollar that you’re applying to buy spare parts for those machines and get it running .<br />
<br />
So when y o u do a combination of all of those items, that’s what is responsible. I will say that we didn’t plan for it, but if you look at exchange loss alone in that financial year, Vitafoam lost over a billion naira to exchange that should have directly gone into our profit and shareholders. If our government is serious, it should be of concern to them.<br />
<br />
What are the ways Vitafoam has benefited from African Continental Free Trade Area?<br />
<br />
What is happening is that we have signed that agreement without looking at all the nuts that bind it together. Nigeria only signed it without wanting other African countries to go ahead of us. We are still trying to sort out all of the tariff elements. How do you treat the origin of goods from one African country to another? These are details that have to be worked out. Some Nigerian businesses are benefiting from the agreement because they have offshore businesses in other countries in Africa. You’ll find that there is an exchange of goods between these Nigerian companies’ offshore businesses and their country of origin. But the real trade has not started because a lot of issues have not been sorted. The good thing is that we have signed the agreement.<br />
<br />
How is multiple taxation affecting the manufacturing sector in Nigeria?<br />
<br />
We in the organised private sector have decided to put our problems in the bucket. I had listed the germane ones but If I had continued, I could have listed multiple taxations. The question we’ve been asking is, what is the Joint Tax Board (JTB) doing? I am sure they have DG, directors and people who go to the office every day that are supposed to be looking at these taxes. These are people who could have looked at these taxes and see how they monetise the tax heads. We have complained, shout and nothing is being done about it. The other time, the Ogun State Government wrote us that we should come and pay a levy for everyone wearing Vitafoam T-shirts; they said that the T- shirt is an advert. They wanted us to pay for the Vitafoam T-shirts in Ogun state. We told them that anybody they see with Vitafoam T-shirts, they should remove it or collect the levy from them. Can you imagine that? All sorts of things going on in the name of generating revenue.<br />
We have told them if they try to muscle the horse that trades the coins, it will die. If the horse dies, the little you are getting will not get to you anymore. We can only continue to engage tax collectors.<br />
<br />
Source : New Telegrapgh Newspaper</p>
