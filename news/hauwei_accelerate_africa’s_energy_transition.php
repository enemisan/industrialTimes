                    <p class="post">President of Huawei sub Saharan Africa, Leo Chen, says “The application of our “4T” technologies, will effectively accelerate Africa’s energy transition toward the direction of ‘4D’, which is Decarbonisation, Digitalisation, Decentralisation, and Democratisation.&quot;<br />
<br />
He made the call on the occasion of the Africa Solar Show 2023 in Johannesburg, where industry associations, sector organisations, industry leaders converged to share industry trends and insights.<br />
<br />
Chen said that in facing  the continent’s power shortages and rising energy prices, Huawei as a leading ICT company, could be part of the  African energy solutions.<br />
<br />
“With a heavy investment in R&amp;D, we’ve developed the most comprehensive set of technologies in ICT, and this forms our unique advantage,” Chen, said.<br />
<br />
He explained that by integrating power electronic and digital technologies, Huawei’s ‘4T’ (WatT, HeaT, BatTery, and BiT) technologies, which refers to Huawei’s innovations in the field of power electronics, thermal management, power storage, and Cloud and AI, could enable the traditional solar energy industry to be more efficient and more intelligent. <br />
<br />
South Africa’s Electricity Minister, Dr. Kgosientso Ramakgopa, gave a keynote address at the forum, and commended Huawei for the role it is playing in infrastructure development in Africa and in greening South Africa’s energy systems<br />
<br />
They used the occasion to presented innovative solutions and best practices which will contribute to the development of the industry on the continent.</p>
