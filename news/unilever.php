            <p class="post">Unilever’s journey began with the founder, Williams Lever.<br />
His vision of making products that solve environmental challenges was evident when he introduced Sunlight bar soap in the early 1890s.<br />
<br />
This milestone helped the Lever Brothers to become the first company to popularize cleanliness in Victorian England.<br />
<br />
In Nigeria, Unilever’s history  dates back to 1923 when Robert Hesketh Leverhulme opened a trading post in Nigeria under the business name, Lever Brothers (West Africa) Ltd.<br />
<br />
The firm was primarily engaged in trading of soap and in 1924, the name was changed to West African Soap Company. <br />
Sensing opportunity in the country, the firm opened a soap factory in Apapa in 1925.<br />
<br />
The company later expanded into the production of food products, it opened a new soap factory in Aba in 1958 and changed its name to Lever Brothers Nigeria Limited in 1955.<br />
In 1960, Lever Brothers introduced Omo detergent into the market, the new product gained traction among buyers, prompting the firm to commission a factory to manufacture Omo detergents in 1964.<br />
<br />
In compliance with the indigenisation decree of 1972, Unilever became a publicly listed company in 1973, selling 60 percent of its shares to the Nigerian public.<br />
The company became a Nigerian owned firm. The change in equity ownership did not affect the firm&#039;s growth.<br />
<br />
In 1982, the firm began producing edible products such as Royco, Blue band and Tree top in Agbara, Ogun State.<br />
<br />
In addition, the company went through a period of mergers and acquisition; acquiring Lipton Nigeria in 1985 and later merging with Vaseline manufacturer, Chesebrough Products Industries in 1988.<br />
<br />
During this period, the company embarked on a backward integration scheme in order to source its raw materials locally. This business decision led the company to invest in crop production and oil palm milling. The company also invested in a tea plantation in Mambilla to provide raw materials for Lipton.<br />
<br />
In 1995, Lever Brothers, 40 percent owned by Unilever merged with Unilever Nigeria Limited, a subsidiary of the Unilever U.K.<br />
<br />
The merger gave Unilever control of the newly merged entity, this was the first time since the indigenisation decree was scrapped that a multinational will have majority equity in a quoted Nigerian company.<br />
<br />
In 2001, the company changed its name to Unilever Nigeria Plc.<br />
<br />
As the company celebrates its  centenary in Nigeria, what should Nigerians look forward<br />
to from the company?<br />
<br />
Carl Cruz, the former  Managing Director of Unilever Nigeria Plc , gave this insights , recently, ( before he left to Airtel Africa Group as MD/CEO).<br />
<br />
&quot; The intent is to be here in the next 100 years and beyond. After being here for a century, Nigerians can expect a better and stronger, Unilever as an organization.<br />
We want to increase our relevance to Nigerian consumers by continuing to offer established products in a refreshed way and bringing in a diverse set of portfolios from other markets that will be genuinely relevant to making the lives of Nigerians much better in the next 100 years and more &quot;, he said .</p>
