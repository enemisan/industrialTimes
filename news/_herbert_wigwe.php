            <p class="post">Herbert Wigwe, the Group Managing Director of Access Bank has been appointed as the President of the France - Nigeria Business Council.<br />
<br />
Wigwe, a seasoned banker and a prominent figure in the Nigerian banking industry, takes over from  Abdul Samad Rabiu, the Pioneer President of the Council/ founder and Executive Chairman of BUA Group.<br />
<br />
During Rabiu’s tenure as the pioneer President, the Council achieved significant milestones in strengthening business ties between France and Nigeria, promoting mutual growth and prosperity. Under his leadership, the Council organized two France-Nigeria Business Forums in Paris, a meeting in Lagos, and a session in Paris last year, which led to several fruitful partnerships between French and Nigerian businesses.<br />
<br />
Rabiu also oversaw the Council’s support for various initiatives fostering collaboration between the brightest minds of both nations and nurturing the next generation of entrepreneurs.<br />
<br />
In his farewell address during the handover ceremony, Rabiu expressed gratitude to the Council members, President Macron, and other stakeholders for their unwavering support during his tenure.<br />
<br />
He acknowledged the exceptional service of Jean Haas, the Secretary of the Council, who will continue in his roles under the new leadership.<br />
<br />
Rabiu expressed confidence in Wigwe’s ability to lead the Council towards new heights, citing his vast experience and keen understanding of the global financial landscape as key assets.<br />
<br />
As Wigwe begins his tenure, Council members have pledged their support and look forward to continued collaboration in fostering sustainable and inclusive economic growth for both France and Nigeria.</p>
