                <section class="post-ctn">
                    <p class="post">The completion of Nigeria&#039;s first privately owned petroleum refinery is great news to the country, and by extension, the African continent, especially in this era of single market economy - African Continental Free Trade Area Agreement (AfCFTA).<br />
<br />
Crude Oil Producer And Exporter Turns Importer<br />
Since 2010, Nigeria is the only OPEC member country that imports more than 90 to 95% of refined petroleum products to meet its domestic consumption.<br />
<br />
For other OPEC member countries, the reverse is the case – virtually every other OPEC member country meets their domestic needs for refined petroleum products through domestic refining.<br />
<br />
Nigeria founds itself in this situation following the grounding of oil refineries established by the Federal Government.<br />
The four FGN-owned refineries had a combined plate capacity of 445,000 bpsd (barrel per stream day), namely the: Warri Refinery and Petrochemical Company (WRPC); Old Port Harcourt Refinery; New Port Harcourt Refinery and Petrochemical Company (PHRC); and Kaduna Refinery and Petrochemical Company (KRPC).<br />
<br />
From the 1960s up to 2010, Nigeria exported over one million barrels per day to the United States of America.<br />
<br />
However, the country was forced into continuous fuel imports due to mismanagement and lack of maintenance of the four refineries.<br />
<br />
Since then, the country has relied on imported petroleum products, with the government subsidising the cost of virtually all the products until recent years when automotive gas oil (also known as diesel) and others were liberalised.<br />
<br />
Data released recently by the National Bureau of Statistics have shown that although, Nigeria has an estimated 37.1 billion barrels of proved crude oil reserves, yet in 2022 alone, the country spent about N10.1 trillion, or $23 billion, on the importation of PMS and other petroleum products.<br />
<br />
Dangote Refinery Changing The Hands of Clock<br />
<br />
However, with the coming onstream of the Dangote Petroleum Refinery, the narrative is about to be changed.<br />
<br />
At the event, Aliko Dangote, the President and CEO of Dangote Group, told the local, continental and global audience that the project is just the beginning of a great journey, a milestone in a new and exciting trajectory for the downstream sector of Nigeria’s Oil and Gas Industry.<br />
<br />
&quot; The refinery will help Nigeria transited from being the largest importer of petroleum products to a net exporter.<br />
<br />
&quot;It is our firm commitment that we will replicate in this sector (Oil and gas), what we have achieved in the cement and fertilizer markets, where Nigeria transited from being the largest importer of these products to a net exporter,&quot; he said.<br />
<br />
He stated that given the 650,000 barrel per day processing capacity, the refinery is more than able to meet all of Nigeria’s domestic fuel consumption, which is about 450,000 barrels per day.<br />
This leaves the excess production of 200,000 bpd available for export.<br />
<br />
He said that the group’s huge investment of over $18.5 billion in the oil and gas industry has been prompted by the desire to support and contribute our quota to the Federal Government’s sustained effort to transform our economy and properly position our country as the leading Nation in Africa, and a respected member among emerging economies in the world.<br />
<br />
“Beyond today’s ceremony, our first goal is to ramp up production of the various products to ensure that within this year, we can fully satisfy our nation’s demand for high-quality products to enable us to eliminate the tragedy of import dependency and stop, once and for all, the dumping in our market of toxic sub-standard petroleum products,&quot; he said.<br />
<br />
He added that beyond the local market, the group intend to ensure that the plants are run at the highest capacity utilization and highest efficiency to enable us to export competitively to other markets, especially in the ECOWAS and the wider Africa region in which 53 countries out of 55 are dependent on imports to meet their petroleum products demand.<br />
<br />
Reports from S&amp;P Global Commodity Insights, gives further insights, noting that with the Dangote Refinery&#039;s first crude batch in June 2023, Nigeria&#039;s petrol production is projected to rise from nearly zero currently to 249,000 barrels per day (b/d) in 2026 and exceed 300,000 b/d by 2033.<br />
<br />
The report also said that Nigeria&#039;s petrol imports will decrease by over half to 154,000 b/d by 2026.<br />
<br />
A representative from the refinery revealed that they expect to commence processing this initial batch, consisting of 500,000 barrels per day (b/d), before gradually increasing production to 650,000 b/d by mid-2024.<br />
<br />
<br />
&quot; With the petroleum Refinery and the Petrochemical plant as well as the Fertilizer plant housed in Nigeria, it invariably implies that there would be no more importation of petroleum products.&quot;<br />
<br />
African Leaders, Private Sector Lauds Dangote<br />
Amidst excitements and cheers, the ex-President of Nigeria, Muhammadu Buhari, and his counterparts from other African countries, the like of President Nana Akufo-Addo of Ghana; Nigerien President, Mohammed Bazoum; and Senegalese President, Macky Sall, applauded Aliko Dangote for his entrepreneurial exploit, and the &quot;can-do spirit of Africa and for<br />
charting the sustainable pathway for African rising&quot;.<br />
<br />
Buhari said that the production capacity of the refinery would enable the country to achieve self-sufficiency in refined products, with some surplus for exports, a milestone for the economy.<br />
<br />
Also, private sector operators the like of the Manufacturers Association of Nigeria (MAN), Lagos Chamber of Commerce and Industry (LCCI), including the Institute of Directors (IoD) and the Nigerian Employers Consultative Association (NECA), praised the billionaire industrialist for the feat.<br />
<br />
Segun Ajayi-Kadir, the Director-General MAN, said &quot; For all the determination and trail-blazing zeal to bring this dream into reality, MAN congratulates Aliko Dangote, on the completion and the official commissioning of this pride-of-Africa birthed-in-Nigeria project.&quot;<br />
<br />
He said that the coming onboard of the Dangote Refinery promises to bring to a pleasant end, the nightmare of long queues at petrol filling stations and the disruption of social and economic activities that come with it.<br />
Similarly, Dr Chinyere Almona, the Director-General of LCCI, noted, “LCCI views the Refinery’s impact on the Nigerian economy as significant. It will save and generate foreign exchange.<br />
<br />
“The Refinery will create jobs, positively affect the value of the Naira, broaden prosperity for the downstream sector, and provide growth opportunities for businesses. It will also stimulate economic growth by impacting the country’s balance of payments.<br />
<br />
“In addition, the Chamber expects the Refinery to fuel further growth and development across its value chain, including cosmetics, plastics, textiles, etc.<br />
<br />
“We also see room for the development of added value in agribusiness, including the Sugar Backward Integration projects that plan to create a strong localized supply in the sugar industry, benefiting local suppliers across the sugar value chain.&quot;<br />
<br />
Wale-Smatt Oyerinde, the Director-General, NECA, added “With the petroleum Refinery and the Petrochemical plant as well as the Fertilizer plant housed in Nigeria, it invariably implies that there would be no more importation of petroleum products; rather, there will be export of finished products, thus, putting an end to long queues and scarcity of petroleum products.<br />
A significant plus of this feat would be the attraction of foreign capital investments that the country desperately needs.&quot;<br />
<br />
So far, so good, in the words of the Chief Executive Officer of the Nigerian National Petroleum Company Limited (NNPCL), Mele Kyari, with the expected completion of the ongoing maintenance of the nation’s four refineries and the just completed Dangote Refinery, Nigeria will no longer import refined petroleum products from mid-2023.<br />
<br />
&quot; With the total production from the national refineries, which are currently being refurbished and will be ready next year, and the products from Dangote Refinery, in which Nigeria has a 20 per cent stake, the country should be able to meet domestic needs without any need for import,&quot; he said.</p>
