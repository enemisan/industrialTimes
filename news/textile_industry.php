                <section class="post-ctn">
                    <p class="post">The textile and fashion industry is a key contributor to global warming, accounting for 1.2 billion tons annually or 10 percent of global carbon emissions.<br />
<br />
<br />
This is disclosed in a report done by the African Development Bank, AfDB.<br />
<br />
It said that textile industry is the second largest industrial polluter after the oil and gas industry.<br />
<br />
Said the report: &quot; Total emissions from textile production, at 1.2 billion tons annually, are more than those of all international flights and maritime shipping combined.<br />
<br />
And it is not getting better: the United Nations framework convention on climate change expects the sector’s emissions to rise by more than 60 percent by 2030.&quot;<br />
<br />
Noting that there is an urgent need for the industry to adopt a circular model with more sustainable practices throughout the value chain, the report said that the textile and fashion industry holds immense potential for economic transformation. It is estimated that the industry today is worth around $1.3 trillion in retail sales.<br />
<br />
&quot; The textile and fashion manufacturing sector is one of the oldest and most technologically complex industries. Its strength stems from its strong production base of a wide range of fibres and yarns, from natural fibres such as cotton, silk, and wool, to synthetic or man-made fibres such as polyester, viscose, nylon, and acrylic. However, every stage in a garment’s life threatens our planet - its resilience, its resources and its people.<br />
<br />
First, the textile industry uses too much water. It can take more than 20,000 litres of water to produce one kilo of cotton, equivalent to a single T-shirt and pair of jeans.<br />
<br />
Second, up to 8,000 different chemicals are used to turn raw materials into clothes. Many of these chemicals cause environmental hazards, including water pollution, and health-related problems.<br />
<br />
The treatments rely heavily on energy consumption, further contributing to greenhouse gas emissions,&quot; said the report.</p>
