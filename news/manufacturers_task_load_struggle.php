                <section class="post-ctn">
                    <p class="post">The Director - General,  Manufacturers Association of Nigeria (MAN), Segun Ajayi-Kadir, said: “As we speak, manufacturers pay over 30 different taxes, levies and fees to agencies of the Federal, State and Local Governments.<br />
Compounding the already difficult situation, there are about 17 new bills aimed at imposing more levies on the manufacturing sector currently before the National Assembly (NASS).<br />
<br />
The taxes<br />
<br />
These include: Company Income Tax; Stamp Duties; Petroleum Profit Tax; Capital Gains Tax; Value Added Tax; Personal Income Tax; Withholding tax; Tertiary Education Tax; 1% of payroll contribution to NSITF; 10% of Payroll contribution to PenCom; 1% of payroll ITF Levy and National Information Development Levy.<br />
<br />
Others are Cabotage Levy; Radio and TV Licenses; Police Special Trust Fund Tax Levy; Niger-Delta Development Commission Levy; National Agency for Science and Engineering Infrastructure levy; Land Use Charge; Parking Fee; Consumption Tax; Road Tax; Standard Organization of Nigeria Fees; Nigeria Content Development Levy; NAFDAC Levy; Nigeria Health Insurance Authority contribution; Signage Fees.<br />
<br />
<br />
MAN, over the years, has expressed dissatisfaction over continuous increases in taxes, excise duties, VAT and others because we believe that multiple taxes/levies/fees depress production in the manufacturing sector.<br />
<br />
“No fewer than 17 bills, aimed at imposing more levies on the manufacturing sector, are pending in the National Assembly.<br />
<br />
These include the National Youth Service Corps Trust Fund Bill (seeking to take 1% of net profit), the Youth Entrepreneurship Development Trust Fund Bill (1% of net profit) and the 2% surcharge on all imports including raw materials, spares and machines).<br />
There is tertiary education tax of 2.5% of the profit; NITDA Levy of 1% of profit; NASENI Levy of 0.25% of profit; Police Trust Fund Levy of 0.005% of profit.<br />
<br />
“It is the preponderance of these taxes and unfriendly policy environment that constrain the competitiveness of the manufacturing sector in the global space and of course the reason for the current ranking of the country on the Ease of Doing Business Ranking.”</p>
