                        <h1>In Nigeria, Manufacturers Struggling with 30 Tax Loads, 17 More to Come</h1>
                        <div class="author-date">
                            <p>Industrial Times</p>
                            <div class="line"></div>
                            <p class="date">12 - 06 - 2023</p>
                        </div>
                    </div>
