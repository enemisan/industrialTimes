                    <p class="post">THE National Agency for Food and Drugs Administration and Control (NAFDAC) has implores members of the public against taking male performance enhancement capsules PrimeZen Black 6000mg capsule alongside its 2000mg; to stop the sale or use, as well as handover the product to the nearest agency office.<br />
<br />
In a statement, NAFDAC Director-General, Prof. Mojisola Adeyeye,<br />
said that the drugs manufactured by Volt Candy is being voluntarily recalled by the agency due to undeclared ingredients they were made of by their manufacturer in America.<br />
<br />
“The recall followed the analysis of Food and Drug Agency (FDA) of the United States of America, an FDA, which found the PrimeZen capsules to contain undeclared tadalafil and sildenafil.<br />
<br />
“Sildenafil and tadalafil are phosphodiesterase (PDE-5) inhibitors found in FDA-approved products for the treatment of male erectile dysfunction.<br />
<br />
“The presence of sildenafil and tadalafil in PrimeZen Black 6000mg capsules renders them unapproved drugs for which safety and efficacy have not been established and, therefore, subject to recall.<br />
<br />
“Risk Statement, PrimeZen Black 6000 capsules containing sildenafil and tadalafil may interact with nitrates found in some prescription drugs (such as nitroglycerin) and may cause a significant drop in blood pressure that may be life-threatening.<br />
<br />
“People with diabetes, high blood pressure, high cholesterol, or heart disease often take nitrates.<br />
<br />
“Among the adult male population who are most likely to use these products, adult males who use nitrates for cardiac conditions are most at risk from these products,” she said.</p>
