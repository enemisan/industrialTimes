                    <p class="post">BUA Foods Plc, a member of BUA Group says it is well-positioned to take advantage of the African Continental Free Trade Area agreement (AfCFTA) through the export of its commodities - rice, sugar, pasta , and edible oil.<br />
<br />
In a statement, the company said that they have invested massively in the sugar industry, rice milling facility, and edible oil plants providing it a platform to keep generating revenues locally and continentaly .<br />
<br />
The company recently enrolled a shipping vessel MV Bundu to accelerate its export expansion efforts.<br />
<br />
Business Expansion<br />
To ensure adequate feedstock to the sugar refining plants and also in response to the Federal government’s backward integration policy in the sugar industry, the Company has invested in sugar estates within Nigeria.<br />
<br />
For example, the company acquired the LASUCO Sugar Company Limited (“LASUCO”) plantation in Lafiagi, Kwara State, which is 20,000 hectares.<br />
<br />
Also, the company’s 200,000 MT per annum rice milling facility in Kano has also been upgraded and commissioned.<br />
<br />
It will be fully commercialized in 2023 providing the company with another source of incremental revenue.<br />
<br />
Further , 250,000 MTPA edible oil plants are expected to be operational in the next 3-5 years.<br />
<br />
During the first quarter of 2023 ,  its top-line revenue rose 60.3% to close at N144.3 billion providing enough buffer to absorb rising operating costs.<br />
<br />
The group’s revenue came from all its 7 major business segments, with every single one of them posting revenue growth quarter on quarter.<br />
For example, its Sugar businesses which include fortified, non-fortified, and molasses contributed N93 billion to revenues compared to N54.1 billion in the same period in 2022. Sales from its fortified segment alone. doubled to about N61 billion.<br />
The non-fortified Sugar business relates to a brand of sugar that is not fortified with Vitamin A and is used typically for industrial consumption. The fortified sugar is fortified with vitamin A and is for domestic consumption.<br />
Also contributing significantly to topline revenues is its Bakery Flour business which grew from N18.3 billion to N29.6 billion in the period under review.<br />
<br />
The company owns an energy-saving and waste-reduction type flour mill in Port Harcourt built for efficiency. It commenced production in 2019 with a capacity of 500,000 MT per annum and plans to increase the capacity to 1.3 MT per annum this year.<br />
<br />
BUA Pasta business which involves the processing, manufacturing, and distribution of pasta such as its IRS premium and slim products also delivered impressive revenue growth. Revenues for the division also rose from N15.9 billion to N19.4 billion.</p>
