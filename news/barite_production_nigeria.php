                    <p class="post">NIGERIA is now producing barite in a bid the Federal Government says is to develop the downstream sector of the mining industry, and to reduce the importation of the mineral into the country.<br />
<br />
Barite is an industrial minerals being used in the manufacturing of cement, rubber, paper, pharmaceutical, cosmetics, and in the oil and gas industry as a weighting agent in drilling mud during drilling operations.<br />
<br />
The Minister of Mines and Steel Development, Arc. Olamilekan<br />
<br />
Adegbite, disclosed this when he  commissioned the N1 billion barite processing plant in Ugaga Community in Yala LGA, Cross River State.<br />
<br />
He explained that before now, the country was exporting the raw barite ore, which would be processed abroad, and was, therefore, losing lots of values because it was not processed locally.<br />
<br />
Adegbite said that the plant will save the country over $300 million annually, which is used to import processed barite for the oil sector.<br />
<br />
â€œHe explained that the choice of Ugaga, as a location for the barite processing plant is predicated on its possession of rich deposits of barite among the South-South State, and its closeness to Port Harcourt, Rivers State, where the processed barites are required for oil exploration in line with local content and value addition policy in Nigeria.<br />
<br />
<br />
In his remarks, the Permanent Secretary in the Ministry, Dr. Oluwatoyin Akinlade, added that the facility will process barite to the International API standard for export to other parts of the world in other to generate foreign exchange.<br />
<br />
Also, the Director of Artisanal and Small Scale Department, Yunusa Mohammed, said artisan barite miners in parts of northern Cross River State in their clusters will now process the resource at the plant for greater wealth addition.</p>
