                    <p class="post">The Chief Executive Officer and Managing Director of the Bank of Industry (BoI), Olukayode Pitan, says<br />
that the development of Special Economic Zones (SEZs) across the country will make it easy for the bank to provide a tailored bundle of financial and non-financial services, including capacity building to micro, small and large enterprises.<br />
<br />
In a statement, he said that it is the mandate of the bank to support Nigerian businesses, and that SEZs provides space where the businesses can perform well.<br />
<br />
&quot; BoI has been playing an appreciable role in improving the country’s business environment by channeling funds to investments that have improved the state of infrastructural development,&#039;&#039; he said .<br />
<br />
Pitan said that SEZs business environment could include adequate infrastructure, an educated and skilled labour force, local input suppliers, tax incentives,  among others.<br />
<br />
“By encouraging manufacturers, suppliers, service providers and firms to co-locate, share common facilities and build well-developed industrial clusters, these and other industries can reduce overhead costs through economies of scale and raise innovation, productivity and global competitiveness,&quot; he said.</p>
