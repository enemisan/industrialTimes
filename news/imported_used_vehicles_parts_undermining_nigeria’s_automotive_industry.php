                    <p class="post">The imports of used vehicles parts and substandard new ones, with an import bill of $3.3 billion annually is not helping the automotive industry in Nigeria to thrives. <br />
<br />
This was disclosed by Luqman Mamudu, the former Director of Planning and Strategy at the National Automotive Design and Development Council (NADDC). <br />
<br />
Mamudu, who is the Chairman, West Africa Automotive Show (WAAS) conference at the Landmark Centre in Lagos, said that the potentials in the country’s automotive industry have been stalled despite the investment and initiatives in local manufacturing of parts and vehicles assembly. <br />
<br />
He noted that instead of maximally exploring the opportunities in the sector, the nation is relying on imports of used parts and substandard new ones, with an import bill of $3.3 billion annually.<br />
He is optimistic that the new conference will raise awareness on issues in the sector and come up with proposals that will help inform the incoming government on ways to invigorate Nigeria’s automotive sector. <br />
<br />
“I expect that the inaugural conference of WAAS and the show itself will deepen demand for new parts in Nigeria. <br />
<br />
“The dominance of used parts potentially undermines any attempt at local manufacturing as they are far cheaper than the original new ones and are perceived as Original Equipment Manufacturer (OEM) standard.” <br />
<br />
“Another adverse impact of the full reliance of fully built automotive import is that it certainly constitutes a strain on the balance of payment position and missed opportunities to create employment. <br />
<br />
“This has the potential to undermine the economy. It is particularly worrying to me because 70 per cent of this is accounted for by pre-owned vehicles and used parts obtained from salvage vehicles,&quot; he said. <br />
<br />
Mamudu saw several strengths in the Nigerian automotive industry, including investment of over $1 billion cumulatively in automotive manufacturing and assembly assets. <br />
<br />
These facilities remain intact in the form of annual installed capacity of over 500,000 vehicles.</p>
