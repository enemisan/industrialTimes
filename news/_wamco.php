                <section class="post-ctn">
                    <p class="post">Ben Langat, the Managing Director, FrieslandCampina WAMCO Nigeria PLC has attributed the growth in the company’s revenues for the financial year ended December 31, 2022, to Products innovation.<br />
<br />
During the period under review, the company’s turnover increased to N324.7 billion in 2022, up 21 percent from N268.4 billion in 2021.<br />
<br />
Langat said that despite declining standard of living among households amid accelerating inflation, the company recorded a profit before tax of N13.5 billion in 2022 as against N12.7 billion in 2021. This represents a six percent increase.<br />
<br />
Peak Yoghurt 1L pack<br />
Langat said that in the year under review, the company introdued Peak Yoghurt 1L pack and successful brand activations like the annual Three Crowns Mum of the Year campaign.<br />
<br />
“We also introduced Nunu yoghurt, Nigeria’s first drinking yoghurt made from 100 percent locally sourced milk and produced in a unique mobile factory.<br />
This is yet another success recorded in the actualization of our sustainability and backward integration plans in support of the dairy development policy of the Federal Government of Nigeria,&quot; he said.<br />
<br />
“We will continue to innovate and drive accessibility to our portfolio by incorporating more local content to drive more competitiveness in the market.<br />
<br />
We remain committed to delivering quality, affordable and accessible nutrition to millions of Nigerian homes and to building a more productive and sustainable future in Nigeria,” Langat added.<br />
<br />
Also, Moyo Ajekigbe, the Chairman Board of Directors, said that the success achieved in 2022 was the result of concerted efforts from various stakeholders.<br />
<br />
He said that in accordance with the company’s dividend policy, the board of directors proposed a total dividend payout of N2.93 per share.</p>
