                    <p class="post">The MTN Group Ltd is investing a $320 million inland fiber cable to connect ten countries on the African continent.<br />
<br />
In a statement, MTN said that the fiber will significantly speed up services such as video streaming and cloud computing once complete, with the last stage expected to be ready in 2025.<br />
<br />
It said that the project will begin with building the East2West link in the fourth quarter of this year, through its MTN GlobalConnect unit and the Africa50 infrastructure investment agency.<br />
<br />
&#039; The project will add about 20,000 kilometers (12,400 miles) of new cable and interconnect over 100,000 kilometers of fiber.<br />
&quot;Africa needs at least 500,000 more kilometers of fiber optic cables, &quot; said MTN.<br />
<br />
The statement further said that the new MTN pipes will be constructed in three phases and run through countries including Kenya, Nigeria and Congo.</p>
