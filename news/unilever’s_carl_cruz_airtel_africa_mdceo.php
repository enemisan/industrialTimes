                    <p class="post">Airtel Africa has named<br />
Carl Cruz as its new Managing Director and Chief Executive Officer of its Nigerian operations.<br />
<br />
Until his recent appointment, Cruz was the MD/CEO  of Unilever in West Africa, with responsibility and oversight of three listed operating companies, including Nigeria, Ghana, and Francophone Africa.<br />
<br />
In a statement, Airtel Africa Group CEO, Segun Ogunsanya, said that the appointment of Cruz takes effect from May 5, this year .<br />
<br />
Ogunsanya said, “Mr. Cruz brings to Airtel Africa a wealth of business experience, exceptional track record and strong values.<br />
<br />
He has a solid record of accomplishment as a strategic and transformational business leader who thrives on problem-solving and building strong teams to deliver business growth.<br />
<br />
&quot; We look forward to working with him to steer our largest region and to deliver on our corporate purpose of transforming lives, ” he said.<br />
<br />
About Cruz<br />
Cruz takes over from C. Surendran, has over 31 years of business and corporate experience from multiple geographies across Africa and Asia.<br />
<br />
He was a board member in the role of Executive Director in Unilever Nigeria Plc and a non–executive director on the board of Unilever Ghana representing Unilever as a shareholder.<br />
<br />
Cruz was the Chairman and Managing Director of Unilever Sri Lanka, in addition to occupying leadership roles in Unilever Philippines and Hindustan Unilever India.<br />
<br />
Throughout his career, Cruz has managed strategic and directional responsibility in sales, distribution, customer and brand development, trade development and commercial engagement.<br />
<br />
He holds a Bachelor’s degree in Marketing Management from the University of De La Salle, Philippines.</p>
