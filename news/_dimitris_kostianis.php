            <p class="post">Dimitris Kostianis will become  the new Chief Executive Officer (CEO) of PZ Cussons Nigeria Plc, effective from June 1, 2023.<br />
<br />
<br />
In a statement, the  Board of Directors of the  company said that Kostianis has spent more than 25 years at PZ Cussons, 13 years of which were in senior leadership roles for PZ Cussons Africa and until his appointment, served as PZ Cussons MD for Asia.<br />
<br />
The Acting company secretary,  PZ Cussons Nigeria,  Olubukola Olonade-Agaga in a signed statement on the Nigerian Exchange Limited (NGX) announced  the retirement of Mr.  Panagiotis (Panos) Katsis as the Chief Executive Officer of the Company and from the Board with effect from  May 31,  2023.<br />
<br />
The Board is grateful for his outstanding performance and for setting the Company on the path of profitability and wishes him the best in his future endeavours.<br />
<br />
PZ Cussons Nigeria Board Chair, Ifueko Omoigui Okauru, said:  “On behalf of the Board I would like to thank Panos for his service to the Nigerian business, where he has played a number of important roles during his 25 years with the PZ Cussons group.<br />
<br />
“He will leave a significant legacy, successfully implementing our simplification strategy and creating the foundations for a strong future. I would also like to welcome Dimitris back to Nigeria, to build on those foundations and to bring his deep experience in the business and the region.<br />
<br />
“Finally, I am delighted to congratulate Oghale and Raj on their appointments to strategic roles that will further facilitate the implementation of our strategy and enable the Nigerian business to leverage more regional opportunities.”<br />
<br />
PZ Cussons Group CEO, Mr. Jonathan Myers,  also said : “I want to add my thanks to Panos for his 25 years of service to the PZ Cussons group, and to welcome the incoming management team who take over at an exciting time in our evolution as a company. I am pleased that for the first time we will have a Nigerian national leading our African consumer business.<br />
<br />
“Panos has built the foundations for a return to profitability and growth in our Consumer business, while continuing to deliver strong performance in the electricals business. We are fully committed to building on those foundations, and excited about the potential for our business in Nigeria as we continue to execute our simplification strategy.”</p>
